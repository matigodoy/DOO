    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = this.getTxtNombre().getText();
        String descripcion = this.getTxtDescripcion().getText();
        if (!nombre.equals("") && !descripcion.equals("")) {
            boolean resultado = controlador.guardar(nombre.toUpperCase(), descripcion.toUpperCase());
            if (resultado) {
                actualizaTabla();
            }
            limpiaVista();
        } else {
            imprimeMensaje(new Exception("Todos los campos son obligatorios"));
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (this.getTblMaterias().getSelectedRow() == -1) {
            imprimeMensaje(new Exception("Debe seleccionar una fila"));
            return;
        }
        int idAnterior = Integer.parseInt(modeloTbl.getValueAt(this.getTblMaterias().getSelectedRow(), 0).toString());
        String nombre = this.getTxtNombre().getText();
        String descripcion = this.getTxtDescripcion().getText();
        if (!nombre.equals("") && !descripcion.equals("")) {
            boolean resultado = controlador.modificar(idAnterior, nombre.toUpperCase(), descripcion.toUpperCase());
            if (resultado) {
                actualizaTabla();
            }
            limpiaVista();
        } else {
            imprimeMensaje(new Exception("Todos los campos son obligatorios"));
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (this.getTblMaterias().getSelectedRow() == -1) {
            imprimeMensaje(new Exception("Debe seleccionar una fila"));
            return;
        }
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro desea eliminar esa materia?", "Información", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.OK_OPTION) {
            int id = Integer.parseInt(this.modeloTbl.getValueAt(this.getTblMaterias().getSelectedRow(), 0).toString());
            boolean resultado = controlador.borrar(id);
            if (resultado) {
                actualizaTabla();
            } else {
                imprimeMensaje(new Exception("Ocurrio un error al eliminar la materia"));
            }
            limpiaVista();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

