    private void tblMateriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMateriasMouseClicked
        if (evt.getSource() instanceof JTable) {
            DefaultTableModel modeloTabla = (DefaultTableModel) this.getModeloTbl();
            if (this.getTblMaterias().getSelectedRow() > -1) {
                this.getTxtId().setValue(modeloTabla.getValueAt(this.getTblMaterias().getSelectedRow(), 0));
                this.getTxtNombre().setText((String) modeloTabla.getValueAt(this.getTblMaterias().getSelectedRow(), 1));
                this.getTxtDescripcion().setText((String) modeloTabla.getValueAt(this.getTblMaterias().getSelectedRow(), 2));
            }
        } 
    }//GEN-LAST:event_tblMateriasMouseClicked

    @Override
    public void setControlador(Controlador c) {
        this.controlador = (MateriaControlador) c;
        this.controlador.cargarTodas(modeloTbl);
        this.lblTotalFilasTabla.setText("Total filas: " + this.modeloTbl.getRowCount());
    }

    @Override
    public void iniciaVista() {
        setLocationRelativeTo(null);
        setVisible(true);
    }

    @Override
    public void imprimeMensaje(Exception... e) {
        if (e.length > 0) {
            JOptionPane.showMessageDialog(this, "Error: " + e[0].getMessage(), "Información", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Operación ejecutada con exito", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    @Override
    public void limpiaVista() {
        this.txtId.setValue(null);
        this.txtDescripcion.setText("");
        this.txtNombre.setText("");
        this.tblMaterias.clearSelection();
    }

    @Override
    public void actualizaTabla() {
        controlador.cargarTodas(modeloTbl);
        this.lblTotalFilasTabla.setText("Total filas: " + this.modeloTbl.getRowCount());
    }

